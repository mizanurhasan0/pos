package Models;

/**
 *
 * @author world
 */
public class BufferProduct {

    private String id, name, type,sku,manufacturer, brand, unit;

    public BufferProduct() {
    }

    public BufferProduct(String id, String name, String type, String sku, String manufacturer, String brand, String unit) {
        this.id = id;
        this.name = name;
        this.type = type;
        this.sku = sku;
        this.manufacturer = manufacturer;
        this.brand = brand;
        this.unit = unit;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSku() {
        return sku;
    }

    public void setSku(String sku) {
        this.sku = sku;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

  
}
